package Programs;

public class SumOFElementsInArray {

	public static void main(String[] args) {
		//  Sum of elements in an array 
		int[]a= {1,2,3,4,5};
		int sum=0;
		for(int i=0;i<a.length;i++)
			
		{
			sum=sum+a[i];
		}
		System.out.println("Sum of elements in array is:"+ sum);
					
		}
	}


